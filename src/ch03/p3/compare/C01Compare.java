package ch03.p3.compare;

public class C01Compare {
    public static void main(String[] args) {

        // 비교 연산자
        // =, !=, >, >= ,< , <=
        // 연산 결과는 boolean (true, false)
        // 다른 타입간 연산 가능 (하지만 가능하면 같은 타입끼리 연산)

        int a = 5;
        int b = 8;

        boolean b1 = a == b;
        System.out.println("b1 = " + b1);
    }
}
