package ch02.lecture.p02type;

public class TstVarEx1 {
    public static void main(String[] args) {
        int num1 = 4;
        int num2 = 3;
        
        System.out.println(4 + 3);
        System.out.println(4 - 3);
        System.out.println(4 * 3);

        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
    }
}
